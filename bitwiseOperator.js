const a = 5;
const b = 2;

// bitwise AND
console.log(a & b); // 0

// bitwise OR
console.log(a | b); // 7

// bitwise XOR
console.log(a ^ b); // 7

// bitwise NOT
console.log(~a); // -6

// left shift
console.log(a << b); // 20

// right shift
console.log(a >> b); // 1

console.log( a>>1); // 2
console.log( a>>2); // 1
console.log( a>>3); // 0
console.log( a>>4); // 0
console.log( a>>5); // 0

console.log( a<<1); // 10
console.log( a<<2); // 20
console.log( a<<3); // 40
console.log( a<<4); // 80
console.log( a<<5); // 160

